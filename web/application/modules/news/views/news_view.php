<h3 id="nova_classificacao">Nova classificação das Imunodeficiências Primárias<br>
<small><small>Postado em: 16/11/2015</small></small></h3>
<p>O <em>Journal  of  Clinical  Immunology</em> publicou  online,  no  mês  de  outubro,  dois  artigos  de
atualização para a classificação das Imunodeficiências Primárias. O comitê de especialistas em 
imunodeficiências  primárias  da  <em>International  Union  of  Immunological Societies</em> (IUIS),  que  se
reúne a cada dois anos para revisar a classificação dessas doenças, incorporou nesta última 
edição 34 novos genes e chega agora a 300 Imunodeficiências Primárias. Os fenótipos estão apresentados 
em 9 tabelas, agrupados de acordo com o componente afetado do sistema imune, com informações dos genes 
relacionados, principais condições imunológicas e não imunológicas associadas e os números para acesso 
ao gene e ao fenótipo na base de dados de herança Mendelian em humanos (<em>Online Mendelian Inheritance in Man - OMIM</em>).</p>
<p>Além do <a href="http://link.springer.com/article/10.1007/s10875-015-0201-1" style="color:#1f76bA">artigo</a> completo da nova classificação 
  o comitê preparou também uma revisão da classificação fenotípica, que organiza os dados de forma mais prática para serem consultados à beira do leito (<a href="http://link.springer.com/article/10.1007/s10875-015-0198-5" style="color:#1f76bA">http://link.springer.com/article/10.1007/s10875-015-0198-5</a>).</p>

