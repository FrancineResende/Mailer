<h3 id="sinais">Fique atento! Sinais de alerta para Imunodeficiências Primárias<br>
<small><small>Postado em: 16/11/2015</small></small></h3>
      <p>Estima­-se que 6 milhões de pessoas no mundo sejam portadoras de algum dos 300 tipos de
Imunodeficiências Primárias. O registro online de Imunodeficiências Primárias da Sociedade 
Latino­-Americana de Imunodeficiências Primárias (<a href="http://registrolasid.org" style="color:#1f76bA">Registro LASID­</a>), 
atualizado em novembro de 2015, conta com apenas 6288 pacientes, que foram 
diagnosticados em 108 centros de referência de 14 países da América Latina. </p>
<p>Cerca de 98% das pessoas que tem uma Imunodeficiência Primária não são 
  diagnosticadas e, portanto, são privadas do acesso a um tratamento específico 
  e que lhes garanta melhor qualidade de vida. Nos casos mais graves, sem o 
  diagnóstico e o tratamento adequado, as crianças vão a óbito antes dos 2 anos de vida.</p>
<p>Para aumentar as oportunidades de diagnóstico a <a href="http://www.info4pi.org" target="_blank" 
  style="color:#1f76bA">Fundação Jeffrey Modell</a> difunde pelo mundo os <strong>10 Sinais de Alerta 
  para Imunodeficiências Primárias</strong>. O material já tem tradução para o português 
  e pode ser encontrado também no site do Grupo Brasileiro de Imunodeficiências Primárias (<a href="http://imunopediatria.org.br" target="_blank" 
  style="color:#1f76bA">BRAGID</a>).</p>
<p><b>Os 10 Sinais de Alerta para Imunodeficiência Primária na <ins>Criança</ins></b> (adaptados para o nosso meio):</p>
 <ol>
  <li>Duas ou mais pneumonias no último ano</li>
  <li>Quatro ou mais otites no último ano</li>
  <li>Estomatites de repetição ou monilíase por mais de dois meses</li>
  <li>Abscessos de repetição ou ectima</li>
  <li>Um episódio de infecção sistêmica grave (meningite, osteoartrite, septicemia)</li>
  <li>Infecções intestinais de repetição / diarréia crônica</li>
  <li>Asma grave, doença do colágeno ou doença auto-imune</li>
  <li>Efeito adverso ao BCG e/ou infecção por Micobactéria</li>
  <li>Fenótipo clínico sugestivo de síndrome associada a imunodeficiência primária</li>
  <li>História familiar de Imunodeficiência Primária</li>
</ol> 
<br>
<p><b>Os 10 Sinais de Alerta para Imunodeficiência Primária no <ins>Adulto</ins></b> (adaptados para o nosso meio):</p>
<ol>
  <li>Duas ou mais novas otites no período de 1 ano</li>
  <li>Duas ou mais novas sinusites no período de 1 ano, na ausência de alergia</li>
  <li>Uma pneumonia por ano por mais que 1 ano</li>
  <li>Diarréia crônica com perda de peso</li>
  <li>Infecções virais de repetição (resfriados, herpes, verrugas, condiloma)</li>
  <li>Uso de antibiótico intravenoso de repetição para tratar infecção</li>
  <li>Abscessos profundos de repetição na pele ou órgãos internos</li>
  <li>Monilíase persistente ou infecção fúngica na pele ou qualquer local</li>
  <li>Infecção por <em>Mycobacterium tuberculosis</em> ou micobactéria atípica</li>
  <li>História familiar de imunodeficiência primária</li>
</ol> 

<p>(fonte: <a href="http://imunopediatria.org.br" style="color:#1f76bA">http://imunopediatria.org.br</a>)</p>

